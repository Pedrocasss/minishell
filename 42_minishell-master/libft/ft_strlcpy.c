#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	p;
	size_t	i;

	p = ft_strlen(src);
	i = 0;
	if (size == 0)
		return (p);
	while (src[i] && i < (size - 1))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (p);
}
